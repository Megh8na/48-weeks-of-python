# Week02 - User inputs and Formatting

# Taking inputs from users

a = input("Enter the first number: ")
b = input("Enter the second number: ")

# type casting

num1 = float(a)
num2 = float(b)

# adding the numbers

results = num1 + num2

# displaying the output using f-string

print(f"The sum of {a} and {b} is {results}")