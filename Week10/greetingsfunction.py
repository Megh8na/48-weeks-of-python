# Week 10: Arguments & Return Values

# Step 1: Define the function
def greet(name, age):
    message = f"Hello {name}! You are {age} years old."
    return message

# Step 2: Call the function with test inputs
greeting1 = greet("Meghana", 25)
greeting2 = greet("Ravi", 30)
greeting3 = greet("Anjali", 18)

# Step 3: Print the results
print(greeting1)
print(greeting2)
print(greeting3)
